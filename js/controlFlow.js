let x = prompt("add a number: ");

if (x >= 17 && x <= 20 ) {
    alert("very well")
} else if (x > 15 && x == 15) {
    alert("good")
}else if (x < 15 && x > 12) {
    alert("not bad")
}else if (x <= 12 && x >= 10) {
    alert("on edge, please try more")
}else if (x < 10 && x >= 0) {
    alert("you are rejected")
}else {
    alert("null")
};


